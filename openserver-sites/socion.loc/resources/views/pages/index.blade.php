    <p>Основы соционики были заложены примерно в 1920 годах, когда Карл Юнг сделал несколько отрытий. Он обнаружил, что люди делятся на экстравертов и интровертов, мыслящих и чувствующих, ощущающих и интуитивных. А именно: одно из качеств у человека выражено больше, как если он лучше работает с логической информацией (мышление), чем с эмоциональной (чувство) и т.д. Эти открытия заложили основу не только соционики, но и многих других типологий.</p>
