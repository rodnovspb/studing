    <p>Рассмотрим аспекты подробнее. Например, возьмем пространство. Чтобы было проще понять этот макроаспект упростим его до слова "границы". Как вы поняли есть черные аспекты, направленные вовне и белые, направленные внутрь. Как проявляются границы вовне? Через воздействие и расширение. Представьте, что вокруг вас есть невидимый ореол личных границ. Это как ваше влияние на окружающий мир. Влияние может увеличиваться и уменьшаться. </p>
    <p>Приведу примеры:</p>
    <ul class="functions__examples">
      <li>Купили дом - получили новую территорию в окружающем мире.</li>
      <li>Заработали много денег - тоже расширили границы влияния за счет ресурсов.</li>
      <li>Накачались в спортзале - теперь когда вы идете по улице на вас уважительно смотрят, а в случае конфликтов стараются не связываться. </li>
      <li>Надели ремень с черепом или сделали татуировку с ним - стали выглядеть отчасти брутальнее.</li>
      <li>Не смогли "постоять" за справедливые интересы - потеряли часть ореола "волевого" человека.</li>
    </ul>
    <p>Вот примерно так и работает этот аспект. В соционике его называют волевой сенсорикой или черной сенсорикой. Хоть и воля - одно из проявлений этого аспекта, но для более глубокого понимания лучше обозначить его для себя как "влияние". Перечислю проявления волевой сенсорики: авторитет, ресурсы, сила, власть, опасность, территория, защита и наказание, яркая или вызывающая одежда, дорогая машина, воля, другие воздействия и многое другое. Все это расширяет границы вашего влияния. Причем вы и сами чувствуете, что границы расширились и другие люди воспринимают это.</p>
    <p>С тем как проявляется волевая сенсорика (воздействие, влияение) разобрались. Она направлена наружу, на расширение границ. А что насчет границ внутрь? Здесь сложного тоже ничего нет, если обозначить для себя такие границы как границы вашего тела. Все тело пронизано нервными окончаниями: тактильными, вкусовыми, обонятельными, зрительными, слуховыми. Внутри тела - тоже нервные окончания. В желудке, кишечнике, в органах и т.д. Все они дают нам <i>ощущения</i>. Расположенные на границах вашего тела эти рецепторы отвечают за <i>восприятие</i>, они связывают тело с окружающим миром. Раньше в волевой сенсорике мы воздействовали на мир, а здесь воспринимаем его, через нервные окончания или ощущения тела. В соционике этот аспект называется сенсорикой ощущений или белой сенсорикой. Продолжение следует.</p>
