    <p>Рассмотрим аспекты подробнее. Например, возьмем пространство. Чтобы было проще понять этот макроаспект упростим его до слова "границы". Как вы поняли есть черные аспекты, направленные вовне и белые, направленные внутрь. Как проявляются границы вовне? Через воздействие и расширение. Представьте, что вокруг вас есть невидимый ореол личных границ. Это как ваше влияние на окружающий мир. Влияние может увеличиваться и уменьшаться. Приведу примеры. Купили дом - получили новую территорию в окружающем мире. Заработали много денег - тоже расширили границы влияния за счет ресурсов. Накачались в спортзале - теперь когда вы идете по улице на вас уважительно смотрят, а в случае конфликтов стараются не связываться. Надели ремень с черепом или сделали татуировку с ним - стали выглядеть опаснее. Вот примерно так и работает этот аспект. В соционике его называют волевой сенсорикой или черной сенсорикой. Хоть и воля - одно из проявлений этого аспекта, но для более глубокого понимания лучше обозначить его для себя как "влияние". Перечислю проявления: авторитет, ресурсы, сила, власть, опасность, территория, защита и наказание, яркая или вызывающая одежда, дорогая машина, воля, другие воздействия и многое другое. Все это расширяет границы вашего влияния. Причем вы и сами чувствуете, что границы расширились и другие люди воспринимают это.</p>
    <p>С тем как проявляется волевая сенсорика (воздействие, влияение) разобрались. Она направлена наружу, на расширение границ. А что насчет границ внутрь? Здесь сложного тоже ничего нет, если обозначить для себя такие границы как границы вашего тела. Все тело состоит из рецепторов: тактильных, вкусовых, обонятельных, зрительных, слуховых. Внутри тела - тоже рецепторы. В желудке, кишечнике, в органах и т.д. Как можно назвать одним словом то, что дают все эти рецепторы? Ощущения (либо восприятие). Границы вашего тела - и есть эти рецепторы, которые связывают ваше тело с окружающим миром. Раньше в волевой сенсорике мы воздействовали на мир, а здесь воспринимаем его, через рецепторы или ощущения тела. В соционике этот аспект называется сенсорикой ощущений или белой сенсорикой. Продолжение следует.</p>
