    <h2 class="functions__title title">Соционические функции и Модель А</h2>
    <p>Давайте подумаем о том что такое мир? Это живые и неживые объекты, расположенные на разных расстояниях. Эти объекты меняются с течением времени. Кроме того у живых объектов есть чувства: эмоциональные состояния и отношения к другим объектам. То есть мир можно описать через: 1) объекты 2) пространство 3) время 4) энергетическое состояние (эмоции и отношения). В соционике эти 4 категории называются <i>макроаспектами</i>.</p>
    <p>Далее каждый макроаспект можно разделить на две половины, как инь-янь. Одна направлена наружу, вторая внутрь. Называются они аспектами, носят сложные названия, пока опишем только макроаспекты и смысл аспектов (увидите их направленность):</p>
    <ul class="functions__macro">
      <li>Объекты: применение и изучение</li>
      <li>Пространство: воздействие и восприятие</li>
      <li>Время: возможности и события</li>
      <li>Энергетическое состояние: эмоции и отношения</li>
    </ul>
    <p>Экстравертные аспекты это: воздействие, применение, расширение. Интровертные: восприятие, изучение, связи. В соционике направленное наружу принято называть экстравертным аспектом (черный цвет), а внутрь - интровертным (белый цвет). </p>
    <p>Аспекты - это информация о мире определенного типа. Обрабатываются они психологическими функциями, имеющими такое же название. Как если есть информационный аспект "волевая сенсорика" и обрабатывается он психологической функцией "волевая сенсорика". </p>
