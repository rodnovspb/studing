      <p>Соционика берет начало в 1920 гг., когда Карл Юнг открыл экстраверсию и интроверсию, а также психологические функции: интуицию, ощущение, мышление и чувствование. Всего у него получилось 8 типов людей, например: экстравертные интуиты, интровертные интуиты и т.д.</p>
      <p>В 1940-х гг. К. Бриггс и ее дочь И. Бриггс-Майерс развили идеи Юнга и открыли уже 16 типов личности. Например, экстравертные интуиты по их теории бывают как мыслительными, так и чувствующими. Эта типология получила известность как MBTI ( Myers–Briggs Type Indicator)</p>
