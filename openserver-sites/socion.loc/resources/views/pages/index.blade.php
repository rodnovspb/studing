    <div class="text">
      <p>По тому, как расположены вышеперечисленные функции в модели психики (<i>Модели А</i> - в честь А. Аугустинавичюте) мы обладаем разными способностями и предрасположенностями.</p>
      <p>Всего существует 16 вариантов расположения соционических функций на этой модели (то есть 16 типов информационного метаболизма или 16 социотипов). Мерность в таблице ниже пока можно понимать как "силу" функции.</p>
      <div class="model_a_img"><img src="{{ asset('storage/images/model-A.jpg') }}" title="Модель А" alt="Соционическая модель А"></div>
      <p>В зависимости от места в модели соционические функции работают по-разному. Например, если в базовой широкий канал восприятия, обработки и реакций, то в болевой функции - канал восприятия узкий, обработка информации вызывает затруднения, реакции могут быть категоричными, вызывать удивление у окружающих.</p>
      <p style="margin-bottom: 3px"> Возьмем, например, социотип "Гамлет":</p>
      <ul class="list__disc">
        <li>Базовая этика эмоций - широкий диапазон эмоций, хорошее умение считывать чувства и манипулировать ими и пр.</li>
        <li>Болевая сенсорика ощущений - сложности с пониманием самочувствия, могут забыть поесть и т.д.</li>
        <li>Внушаемая структурная логика - любят когда им раскладывают по полочкам сложные понятия и т.д.</li>
      </ul>
