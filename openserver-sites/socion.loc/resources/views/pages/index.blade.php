  <h2 class="page__subtitle">Теория соционики для самых маленьких</h2>
  <p>Соционика выделяет в человеческой психике 4 психологические оси ("юнговские" дихотомии):</p>
  <ul class="beginners__functions ordered">
    <li>Сенсорика - интуиция</li>
    <li>Логика - этика</li>
    <li>Экстраверсия - интроверсия</li>
    <li>Рациональность - иррациональность</li>
  </ul>
    <p>В зависимости от того, что в человеке более выражено его относят к логикам или этикам, интуитам или сенсорикам и т.д. Опишем полюсы этих осей общими словами, чтобы было примерное представление:</p>
    <ul class="ordered">
      <li>Сенсорика - восприятие действительности через ощущения от органов чувств</li>
      <li>Интуиция - восприятие действительности через образы, идеи, абстракции</li>
      <li>Логика - восприятие действительности через правила, факты</li>
      <li>Этика - восприятие действительности через отношения, чувства</li>
      <li>Экстраверсия - направленность мышления на внешние объекты</li>
      <li>Интроверсия - направленность мышления на внутренний мир</li>
      <li>Рациональность - преобладают установки о "правильной" действительности</li>
      <li>Иррациональность - выражена подстройка под меняющиеся обстоятельства</li>
    </ul>
    <p>Все люди в зависимости от обстоятельств проявляют эти качества, но все же можно заметить сдвиг в сторону одного полюса. Кто-то получается больше логик, чем этик, больше сенсорик, чем интуит и т.д. Набор таких признаков создает <i>социотип</i>. Например: логик-сенсорик-иррационал-экстраверт.</p>
    <h3>Социотипы</h3>
    <p>Исследователи заметили, что люди, обладающие одинаковым набором признаков похожи друг на друга по мышлению и поведению. На основе этого наблюдения в 1940-х годах была создана типология Майерс — Бриггс описывающая 16 типов личности и опросник MBTI (Myers-Briggs Type Indicator) определяющий признаки.</p>
    <p>С течением времени стало понятно, что существует еще более обширная и подробная модель человеческой психики (например, интуиция проявляется как <i>интуиция времени</i> и <i>интуиция возможностей</i>) и явление <i>информационного метаболизма</i>. Получаются те же 16 типов личности, примерно похожие на типы личности по MBTI (так как сохраняется общая основа в виде дихотомий Юнга), но исследуются они более глубоко и детально, включая структуру личности, реакции, информационный обмен с миром и взаимоотношения между людьми.</p>
