    <p><b class="bold">Прежде чем изучать соционику</b> нужно понять, что человек существо сложное, личность и ее проявления зависят от наследственности, воспитания, образования, опыта, испытаний, пй-типа (порядок Логики, Воли, Физики и Эмоции) и даже настроения. Поэтому так сложно типироваться по тестам и даже очно. Соционика описывает теорию, но вас будет ждать много неожиданных поворотов, когда ожидали одно, а видите другое. Происходит это, в том числе потому, что в человеке есть все соционические функции и работают они сообща.</p>
