    <h2>Создание соционики</h2>

      <p>В 1960-1970-х гг. польский психиатр А. Кемпински выдвинул идею о том, что наряду с энергетическим обменом с окружающей средой (метаболизм веществ), у человека происходит также информационный обмен. Например, при аутизме такой обмен прекращается. </p>
      <figure class="definition__figure">
      <img src="{{ asset('storage/images/sozdatelnica_socioniki.jpg') }}" title="Создательница соционики А.Аугустинавичюте" alt="Создательница соционики А.Аугустинавичюте">
      <figcaption><span>Создательница соционики А.Аугустинавичюте</span></figcaption>
      </figure>
      <p class="text_around_img">Объединив эту идею с типологией Юнга литовский экономист и социолог А. Аугустинавичюте в 1970-х гг. создает соционику - теорию об информационном метаболизме и структуре личности человека.</p>
      <p>А. Аугустинавичюте с единомышленниками описала в окружающем мире 8 аспектов информации, каждый из которых обрабатывается соответствующей функцией психики.</p>
      <p>Функции (т.е. обработчики информации) в психике человека расположены по иерархии. От места расположения функции зависят способности человека к работе с соответствующей информацией.</p>
      <p>Например, кто-то быстро считывает одни сигналы, но испытывает сложности с другими (человек может умело сыграть любую эмоцию, но не понимает вовремя, что заболел). Одни рассеяны и забывчивы на одно, другие на другое.</p>
      <h4>Другие примеры</h4>
      <ul class="list__disc">
        <li>хорошие деловые качества, но неумение рассчитывать время (отсюда страх опоздать, вечная спешка)</li>
        <li>видение времени как на ладони, но сложности с эффективным порядком действий (отсюда страх начинания нового дела)</li>
        <li>хороший аналитический ум, но непонимание степени опасности (могут оставлять вещи там, где их украдут)</li>
        <li>умение разбираться в людях, но сложности с аналитической информацией (сложно читать таблицы, графики)</li>
        <li>способность придумывать необычные идеи, но непонимание отношений (могут обидеть по незнанию) и т.д.</li>
      </ul>
